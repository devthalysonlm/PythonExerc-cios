'''Crie um programa que leia um número inteiro e mostre na tela se ele é PAR ou IMPAR'''


print('\033[4;32;40mDESCOBRINDO NÚMEROS PARES E ÍMPARES!\033[m')
num = int(input('\033[35mDigite um número qualquer:\033[m'))
if num % 2 == 0:
    print('\033[1;34mNÚMERO PAR!!\033[m')
else:
    print('\033[33mNUMERO ÍMPAR!!\033[m')














