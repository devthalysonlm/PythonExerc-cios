'''Crie um programa que leia o nome completo de uma pessoa e mostre:
> O nome com todas as letras maiúsculas. > O nome com todas minúsculas
> Quantas letras ao todo sem considerar espaços . > Quantas letras tem o primeiro nome'''

name = str(input('Qual seu nome?')).strip()
print('O nome com todas a letras maiúsculas é {}'.format(name.upper()))
print('O nome com todas a letras minúsculas é {}'.format(name.lower()))
print('O nome tem ao todo {} letras'.format(len(name) - name.count(' ')))
print('O primeiro nome tem {} letras'.format(name.find(' ')))




























































































